// Tag: Basic calculation functions
function basicSubstract( a, b )
{
  return ( a - b );
}

function basicAddition( a, b )
{
  return ( a + b );
}

function basicMultiply( a, b )
{
  return ( a * b );
}

function basicDivide( a, b )
{
  return ( a / b );
}